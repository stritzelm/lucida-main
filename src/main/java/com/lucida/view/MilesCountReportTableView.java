package com.lucida.view;

public class MilesCountReportTableView {
    private int monthlyMiles;
    private int annualMiles;

    public MilesCountReportTableView() { }

    public int getMonthlyMiles() {
        return monthlyMiles;
    }

    public void setMonthlyMiles(int monthlyMiles) {
        this.monthlyMiles = monthlyMiles;
    }

    public int getAnnualMiles() {
        return annualMiles;
    }

    public void setAnnualMiles(int annualMiles) {
        this.annualMiles = annualMiles;
    }
}
